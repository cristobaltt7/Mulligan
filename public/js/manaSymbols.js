// Creamos la libreria de iconos
let mapa = new Map();

for (const objeto of libreria) {
    mapa.set(objeto.symbol, objeto.svg_uri);
}

// Funcion que modifica la cadena de simbolos de mana y añade las imagenes correspondientes
export function pintarSimbolos (cadena){
    for (const simbolo of mapa) {
        if(cadena.includes(simbolo[0])) {
            cadena = cadena.replaceAll(simbolo[0],` <img class="icono-mana" src="${simbolo[1]}" alt="${simbolo[0]}">`);
        }
    }
    return cadena;
}